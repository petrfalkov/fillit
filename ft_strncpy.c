#include "fillit.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	char	*s;

	s = dst;
	while (n > 0 && *src != '\0')
	{
		*s = *src;
		s++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*s = '\0';
		s++;
		n--;
	}
	return (dst);
}
